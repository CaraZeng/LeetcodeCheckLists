# Find Bottom Left Tree Value (recursive)

# recursion
# Key Note:
# 1. why we increase depth before recursion and decrease it after recursion?
# we need to increase 1 to depth to traverse next level, and if we dont decrease it,
# we may keep traversing down, and forget that maybe the node has right subtree to traverse.
# And acutally decrease 1 wont interfere with traverse because we decrease it after traverse.

# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def findBottomLeftValue(self, root: Optional[TreeNode]) -> int:
        self.max_depth = float('-inf')
        self.result = None
        self.traversal(root, 0)
        return self.result
    
    def traversal(self, node, depth):
        if not node.left and not node.right:
            if depth > self.max_depth:
                self.max_depth = depth
                self.result = node.val
            return
        
        if node.left:
            depth += 1
            self.traversal(node.left, depth)
            depth -= 1
        if node.right:
            depth += 1
            self.traversal(node.right, depth)
            depth -= 1

